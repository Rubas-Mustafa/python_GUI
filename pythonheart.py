import turtle

turtle.pensize(5)
turtle.speed(1)
turtle.color('purple')
turtle.begin_fill()
turtle.fillcolor('lightblue')
turtle.bgcolor('pink')
turtle.left(140)
turtle.forward(180)
turtle.circle(-90, 200)
turtle.setheading(60)
turtle.circle(-90, 200)
turtle.forward(180)
turtle.end_fill()

turtle.mainloop()
